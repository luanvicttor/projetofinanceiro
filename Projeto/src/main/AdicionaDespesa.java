    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        java.util.GregorianCalendar calendar = (java.util.GregorianCalendar) jCalendar.getCalendar();
        
        int year = calendar.get(java.util.Calendar.YEAR);
        int month = calendar.get(java.util.Calendar.MONTH) + 1; // Note que os meses s√£o base 0 (janeiro √© 0)
        int day = calendar.get(java.util.Calendar.DAY_OF_MONTH);

        java.util.Calendar newCalendar = java.util.Calendar.getInstance();
        newCalendar.set(java.util.Calendar.YEAR, year);
        newCalendar.set(java.util.Calendar.MONTH, month);
        newCalendar.set(java.util.Calendar.DAY_OF_MONTH, day);
        
        java.util.Date date = newCalendar.getTime();
        String tipo = (String) txtTipo.getSelectedItem();
        float valor = Float.parseFloat(txtValorDespesa.getText());
        valor = valor * -1;
        System.out.println(tipo + " " + valor + " " + date);
        controller.AtualizaSaldo(valor);
        //dashboard.atualizarSaldo();
        
        
    }//GEN-LAST:event_btnSalvarActionPerformed

